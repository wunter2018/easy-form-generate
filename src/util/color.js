export function matchThemeColor (theme) {
  switch (theme) {
    case 'element-blue':
      return '#409eff'
    case 'orange-red':
      return '#FF4500'
    case 'sea-green':
      return '#2e8b57'
    case 'dark':
      return '#0f1011'
    case 'dark-magenta':
      return '#8B008B'
    case 'crimson':
      return '#DC143C'
  }
}
